CREATE TABLE KAFKA_MEDICAL_ALERTS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ALERT_ID VARCHAR2(36) NOT NULL,
    SOURCE_EVENT_ID VARCHAR2(36) NOT NULL,
    PATIENT_ID NUMBER NOT NULL,
    ANOMALY_TYPE VARCHAR2(50) NOT NULL,
    MEASURED_VALUE NUMBER(10, 2) NOT NULL,
    MINIMUM_THRESHOLD NUMBER(10, 2) NOT NULL,
    MAXIMUM_THRESHOLD NUMBER(10, 2) NOT NULL,
    DETECTED_AT TIMESTAMP WITH TIME ZONE NOT NULL,
    CREATED_AT TIMESTAMP WITH TIME ZONE NOT NULL,

    CONSTRAINT PK_KAFKA_MEDICAL_ALERTS
        PRIMARY KEY (ID),

    CONSTRAINT UK_KAFKA_MEDICAL_ALERT_ID
        UNIQUE (ALERT_ID)
);

CREATE INDEX IDX_KAFKA_ALERT_PATIENT
    ON KAFKA_MEDICAL_ALERTS (PATIENT_ID);

CREATE INDEX IDX_KAFKA_ALERT_DETECTED
    ON KAFKA_MEDICAL_ALERTS (DETECTED_AT);